  <!-- Matte Lipstick -->
  <h2 style="margin-top:30px;font-weight:600;">Matte Lipstick Tutorials</h2>

  <h3 style="margin-top:15px;">1. Choosing the Right Lipstick Shade</h3>
  <p>Each shade creates a unique impression. Select the tone that complements your style.</p>
  <ul style="margin-top:10px;">
    <li><strong>Gloss (Light Nude Pink)</strong> — natural, soft glam looks</li>
    <li><strong>Vivid (Peach/Orange)</strong> — fresh, lively, bright makeup styles</li>
    <li><strong>Berry (Pink)</strong> — playful and youthful tone</li>
    <li><strong>Nude (Classic Nude)</strong> — versatile neutral shade for any look</li>
  </ul>
  <p style="margin-top:5px;color:#555;">Tip: Exfoliate and moisturize lips before application for a smooth matte finish.</p>

  <h3>2. Applying Matte Lipstick Properly</h3>
  <ul>
    <li>Prep lips with balm or primer</li>
    <li>Outline with lip liner for definition (optional)</li>
    <li>Apply from center towards the edges</li>
    <li>Blot lightly for longer wear</li>
  </ul>
  <p style="color:#555;">For gradient lips, apply at the center and blend outward using your fingertip.</p>


  <!-- Foundation -->
  <h2 style="margin-top:35px;font-weight:600;">Foundation Tutorials</h2>

  <h3 style="margin-top:15px;">3. Finding Your Perfect Shade</h3>
  <ul>
    <li><strong>Pebble</strong> — fair skin tone</li>
    <li><strong>Neutral</strong> — balanced undertones</li>
    <li><strong>Ivory</strong> — medium to tan skin tones</li>
    <li><strong>Toupe</strong> — deeper skin tones</li>
  </ul>
  <p style="color:#555;">Tip: Test foundation on the jawline and view under natural light for accurate matching.</p>

  <h3>4. Seamless Foundation Application</h3>
  <ul>
    <li>Start with hydrated skin</li>
    <li>Use primer for improved longevity</li>
    <li>Blend foundation evenly with sponge or brush</li>
    <li>Set with powder to control shine</li>
  </ul>
  <p style="color:#555;">For a lighter natural finish, use a damp sponge. For full coverage, build slowly in layers.</p>


  <!-- Blush -->
  <h2 style="margin-top:35px;font-weight:600;">Milk Tint Liquid Blush Guide</h2>

  <h3 style="margin-top:15px;">5. Selecting a Blush Shade</h3>
  <ul>
    <li><strong>Sweet Pea (Rosy Pink)</strong> — soft natural flush</li>
    <li><strong>Lychee (Light Pink)</strong> — fresh and subtle tone</li>
    <li><strong>Peachy (Light Peach)</strong> — everyday glow</li>
    <li><strong>Coral (Nude Orange)</strong> — warm sun-kissed finish</li>
  </ul>
  <p style="color:#555;">Tip: Matching your blush and lip tone creates a cohesive look.</p>

  <h3>6. Natural Liquid Blush Application</h3>
  <ul>
    <li>Place 2–3 small dots on each cheek</li>
    <li>Blend using fingertips, sponge, or brush</li>
    <li>Add more layers for intensity</li>
    <li>Swipe lightly across the nose for a soft youthful effect</li>
  </ul>
  <p style="color:#555;">Professional note: Can also be used on lips for a monochromatic finish.</p>


  <!-- Eyes -->
  <h2 style="margin-top:35px;font-weight:600;">Eye Makeup Basics</h2>

  <h3 style="margin-top:15px;">7. Essential Eye Products</h3>
  <ul>
    <li><strong>Eyeliner</strong> — defines and shapes the eyes</li>
    <li><strong>Mascara</strong> — adds volume and length</li>
    <li><strong>Cluster Lashes</strong> — customizable from natural to dramatic</li>
    <li><strong>Eyeshadow</strong> — enhances depth with color and blending</li>
  </ul>

  <h3>8. Simple Everyday Eye Look</h3>
  <ul>
    <li>Start with a neutral base shade</li>
    <li>Deepen the crease with a slightly darker tone</li>
    <li>Apply a thin line of eyeliner for lift</li>
    <li>Add mascara for definition</li>
    <li>Optional: add lashes for a refined finish</li>
  </ul>
  <p style="color:#555;">Tip: A nude or white liner on the lower waterline brightens the eyes.</p>


  <!-- Beauty Details -->
  <h2 style="margin-top:35px;font-weight:600;">General Beauty Tips</h2>
  <ul>
    <li>Use lip tint as blush for a quick minimalist look</li>
    <li>Blend upward for a lifted effect</li>
    <li>Set base makeup with translucent powder to prevent creasing</li>
    <li>Pat liquid products instead of dragging them</li>
    <li>Remove makeup gently to maintain skin health</li>
  </ul>

